<p><strong>ஶ்ரீமத் ராமாயண நவாஹம், டெல்லி</strong></p>
<p>5-3-2015 முதல் 13-3-2015 வரை டெல்லி அகிலபாரத ஸாது ஸங்கத்தின் சார்பில் தசரதபுரி, ராஜாராம் மந்திரில் ஶ்ரீமத் ராமாயணம் ப்ரவசனம், ராகவசதக பஜனை, திவ்யநாம ஸங்கீர்த்தனம் முதலிய வைபவங்கள் ஶ்ரீஶ்ரீ அண்ணாவின் முன்னிலையில் நடைபெறும்.</p>
<p>&nbsp;</p>
<p><strong>ஶ்ரீ ராம நவமி உத்ஸவம், சேங்கனூர்.</strong></p>
<p>26-03-2015 முதல் 28-03-2015 வரை சேங்கனூர், ஶ்ரீ ராம நவமி உத்ஸவம் நடைபெறும். திருவிசலூர் ஶ்ரீ ராமக்ருஷ்ணபாகவதர் தலைமையில் ஸீதா கல்யாணம், வஸந்தோதஸவம் நடைபெறும்.</p>
