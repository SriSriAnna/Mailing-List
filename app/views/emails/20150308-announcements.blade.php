<p class="lead"><span style="text-decoration: underline;"><strong>செய்திகள்</strong></span></p>
<p><strong>மஹா சிவராத்ரி, பண்டரிபுரம்.</strong></p>
<p>17-03-2015 ஆம் தேதி மஹாசிவராத்ரியை முன்னிட்டு ஶ்ரீ ஶ்ரீ அண்ணாவின் பண்டரி மஹா ஷேத்ரத்தின் வார்ஷிக பூஜையும் வீதிப்ரதஷிணம், கதாப்ரவசனம், நாமஸ்ங்கீர்த்தனம் முதலிய உத்ஸவங்கள் வழக்கம்போல் சிறப்பாக நடைபெற்றன.</p>
<p>&nbsp;</p>
<p><strong>டெல்லி ப்ரேகிக வித்யா கேந்த்ரம்.</strong></p>
<p>21-02-2015 முதல் 23-02-2015 மூன்று நாட்கள் டெல்லி குர்கான் ப்ரேமிக வித்யா கேந்த்ரத்தின் ஆண்டு விழாவை முன்னிட்டு&nbsp;கதாப்ரவசனம், நாமஸ்ங்கீர்த்தனம்&nbsp;முதலிய வைபவங்கள் ஶ்ரீஶ்ரீ அண்ணாவின் முன்னிலையில்&nbsp;நடைபெற்றன.</p>
<p>&nbsp;</p>
<p><strong>ஹோலி உத்ஸவம், ஶ்ரீ பிருந்தாவனம்</strong>.</p>
<p>24-02-2015 முதல் 04-03-2015 வரை ஶ்ரீ பிருந்தாவனம் ப்ரேமமாதுரி குஞ்ஜத்தில் ஶ்ரீ ஶ்ரீ அண்ணா முன்னிலையில் ஹோலி உத்ஸவம் நடைபெற்றது.</p>
