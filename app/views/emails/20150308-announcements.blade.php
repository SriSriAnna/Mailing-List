<p>19-03-2015 முதல் 25-03-2015 வரை தினசரி மாலை 6:30 மணிக்கு சென்னை, கே.கே.நகர் ஶ்ரீராம பக்த ஜனஸமாஜ் சார்பில் மேற்கு கே.கே.நகர், வேம்புலி அம்மன் கோவில் தெரு, மீனாஷி பொறியியல் கல்லூரி வளாகம் &nbsp;A.N.R ஹாலில் ஶ்ரீ ஹரி அண்ணா அவர்களால்&nbsp;ஶ்ரீமத் பாகவத ப்ரவசனம் நடைபெறும்.</p>
