<h1 class="chapter">2</h1>.
<blockquote>Punye Bhaktapurikshetre Ramye Venanaditate<br />
Akshwattha Vrikshamulepi Vraghracharmasane Shive</blockquote>

<br />
<p>Shri Sadgurunatha, lotus-eyed, the majestic and the virtuous, was seated on a tiger-skin mat in a Yogic posture of Veerasana, under a Pipal tree, on the pleasant bank of river Vena, in the holy center of the renowned Paranoor, called Bhaktapuri.</p>
<hr />
<h1 class="chapter">3</h1>.
<blockquote>Veerasane Samaseenam Padmaksham Purushottamam <br />
Tejasvinam Suprasannam Kambugrivam Mahabhujam.</blockquote>

<br />
<p>His countenance was illuminated with divine bliss. His neck was elegantly long like the holy conch. He had delightfully long arms.</p>
<hr />
<h1 class="chapter">4</h1>.
<blockquote>Vishala Vakshasam Dheeram Vineetam Shyamalachivam <br />
KrishnaBhaktirasonmattam Bashpasusnigdhalochanam.</blockquote>

<br />
<p>He had a broad chest. He was dark like the clouds. He was adorned with the qualities of courage and humility. He was overwhelmed by the luscious sweetness of Krishna bhakti (devotion to Lord Krishna) and hence his eyes were filled with tears of joy and his hair stood on end.</p>
<hr />
<h1 class="chapter">5</h1>.
<blockquote>Pulakankitagatrascha Punyashlokam Kripanidhim<br />
Gurudevam Namaskritya Paprachu Vaishnavottamah</blockquote>

<br />
<p>The bhagavatottamas, the learned disciples, paid their respects to this illustrious and compassionate Sadguru and started conversing with him humbly.</p>
